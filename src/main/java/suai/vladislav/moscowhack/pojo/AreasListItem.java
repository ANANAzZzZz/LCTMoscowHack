public class AreasListItem{
	private String borderColor;
	private String color;
	private String name;
	private String id;
	private String points;

	public String getBorderColor(){
		return borderColor;
	}

	public String getColor(){
		return color;
	}

	public String getName(){
		return name;
	}

	public String getId(){
		return id;
	}

	public String getPoints(){
		return points;
	}
}
